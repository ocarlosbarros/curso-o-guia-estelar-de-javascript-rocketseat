//Operadores de atribuição
let x;

console.log(x);//undefined

//assigment
x = 1
console.log(x)//1

//addition assigment
x += 2;
console.log(x);//3

//subtraction assigment
x -= 1;
console.log(x)//2;

//multiplication assigment
x *= 2;
console.log(x);//4

//division assigment
x /= 2;
console.log(x);//2

//remainder 
x %= 2;
console.log(x);//0

//exponentiation
x **= 2
console.log(x);//0