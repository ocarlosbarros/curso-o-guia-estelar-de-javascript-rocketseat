let button = 'q';

switch ( button ) {
    case 'a':
    console.log('Andar para esquerda');
    break;
    case 'd':
    console.log('Andar para direita');
    break;
    case's':
    console.log('Andar para baixo');
    break;
    case'w':
    console.log('Andar para cima');
    break;
    default:
        console.log('Idle');
        break;
}
